<?php

namespace App\Http\Controllers;

use Illuminate\Http\Request;
use App\Cliente;

class ClienteController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
    	$clientes = Cliente::all();
        return view('index', compact('clientes') );
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('create_cliente');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $dados = $request->all();
        Cliente::create($dados);
        return back()->with(['success' => 'Cliente cadastrado com sucesso!']);
        //dd(Cliente::where('id', 1)->toSql());
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $editaClientes = Cliente::findOrFail($id);
        return view('/edit_cliente', compact('editaClientes') );
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {	
    	$clientes = $request->all();
     	$id = Cliente::findOrFail($id);
     	$id->update($clientes);
     	return back()->with(['success' => 'Cliente Atualizado com sucesso!']);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
       
     	$id_cliente = Cliente::findOrFail($id);
     	$id_cliente->delete($id);
     	return back()->with(['success' => 'Cliente Excluido com sucesso!']);
    }
}
